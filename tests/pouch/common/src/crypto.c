/** Load the raw private key data into PSA */
static psa_key_id_t import_raw_pk(const uint8_t *private_key, size_t size)
{
    mbedtls_pk_context pk;
    mbedtls_pk_init(&pk);
    int err = mbedtls_pk_parse_key(&pk,
                                   private_key,
                                   size,
                                   NULL,
                                   0,
                                   mbedtls_psa_get_random,
                                   MBEDTLS_PSA_RANDOM_STATE);
    if (err)
    {
        return PSA_KEY_ID_NULL;
    }

    psa_key_attributes_t attrs = PSA_KEY_ATTRIBUTES_INIT;

    psa_set_key_algorithm(&attrs, PSA_ALG_ECDH);
    psa_set_key_type(&attrs, PSA_KEY_TYPE_ECC_KEY_PAIR(PSA_ECC_FAMILY_SECP_R1));
    psa_set_key_usage_flags(&attrs, PSA_KEY_USAGE_DERIVE);

    psa_key_id_t key_id;
    err = mbedtls_pk_import_into_psa(&pk, &attrs, &key_id);
    if (err)
    {
        return PSA_KEY_ID_NULL;
    }

    return key_id;
}


const struct pouch_config *setup_credentials(void)
{
    const struct pouch_cert server_cert = {
        .buffer = server_cert_der,
        .size = sizeof(server_cert_der),
    };
    zassert_ok(pouch_server_certificate_set(&server_cert));

    psa_key_id_t key = import_raw_pk(device_key_der, sizeof(device_key_der));
    zassert_true(key != PSA_KEY_ID_NULL);

    pouch_test_config.private_key = key;

    return &pouch_test_config;
}
